    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) accountTable.getModel();
        model.setRowCount(0);

        for(UserAccount userAccount: ecosystem.getUserAccountDirectory().getUserAccountList()){
            Object[] row = new Object[5];
            row[0] = userAccount.getUsername();
            row[1] = userAccount.getPassword();
            row[2] = userAccount.getAddress();
            row[3] = userAccount.getPhone();
           // String[] role = userAccount.getRole().toString();
            row[4] = userAccount.getRole().toString();
            model.addRow(row);
            
        }
    }

