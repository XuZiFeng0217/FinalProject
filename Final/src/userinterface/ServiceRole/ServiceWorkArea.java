    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for (Problem problem : ecosystem.getProblemDirectory()){
            Object[] row = new Object[2];
            row[0] = problem;
            row[1] = problem.getAnswer();
            
            model.addRow(row);
        }
    }
